'''测试参数的类型：默认值参数、命名参数、位置参数'''
#默认值参数
def test01(a,b,c,d):
    print("a是：{0}，b是{1}，c是{2}，d是{3}".format(a,b,c,d))

test01(10,20,30,40)
# test01(10,20) # 会报错的

#命名参数
test01(a=12,c=90,d="hello",b=3.14)

#位置参数
def test02(a,b,c=100,d='中国'):  #默认值参数必须位于普通位置参数后面
    print("a是：{0}，b是{1}，c是{2}，d是{3}".format(a, b, c, d))

test02(50,60)
test02(50,60,70)

